        <h1 class="name">Josh Thomas</h1>
        <p class="introduction"><br>After making his mark as an award-winning and hugely-successful stand-up comedian and television panellist, 
                <b>Josh Thomas</b> turned his hand to writing, acting and co-executive producing his own television show for ABC TV, 
                inspired by his life and his comedy, called <em>Please Like Me</em>.</br>
                <br>When Participant Media in the US chose to launch its brand new Pivot television network with 
                <em>Please Like Me</em> and to co-commission a second season with ABC TV, 
                Josh was on his way to being an international success story.</br>
                <br>He has been awarded by the Australian Academy (AACTA) and the Australian Writers Guild for his writing for <em>Please Like Me</em>,
                and has been nominated for AACTA and Logie Awards for his performances in the show. In addition, 
                the show itself has won Best Comedy at the AACTA Awards and been nominated as Best Comedy at the 
                International Emmy Awards, Rose d'Or Awards, GLAAD Media Awards and Logie Awards. The show has gained critical 
                acclaim across the globe, and has been named as one of the best TV shows of year by Entertainment Weekly, The New Yorker, 
                Time, The A.V. Club and the Los Angeles Times.</br>
                <br>Following on from the show's success in both Australia and the US, 
                <em>Please Like Me</em> has also been licensed to air in Germany, France, Sweden, Canada, Finland and Israel.</br>
